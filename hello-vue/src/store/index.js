import Vue from 'vue'
import Vuex from 'vuex'

// 使用插件
Vue.use(Vuex)

const store = new Vuex.Store({
  state: {
    count: 0
  },
  mutations: {
    increment (state) {
      state.count++
    },
    decrease (state) {
      state.count--
    }
  }
})

export default store

